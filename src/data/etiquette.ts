// ─── HELPERS ─────────────────────────────────────────────────────────────────

export function getByCategory(category: EtiquetteCategory): EtiquetteItem[] {
  return ETIQUETTE_DATA.filter((item) => item.category === category);
}

export function getByGender(gender: 'male' | 'female'): EtiquetteItem[] {
  return ETIQUETTE_DATA.filter(
    (item) => item.applicableTo === 'all' || item.applicableTo === gender,
  );
}

export function getByMosque(mosque: 'haram' | 'nabawi'): EtiquetteItem[] {
  return ETIQUETTE_DATA.filter(
    (item) => item.mosque === mosque || item.mosque === 'both' || item.mosque === 'general',
  );
}

